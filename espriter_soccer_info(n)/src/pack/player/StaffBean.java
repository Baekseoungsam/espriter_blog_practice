package pack.player;

public class StaffBean { 
	private int stnum;
	private String stname, stposition, stnatl, ststory;
	public int getStnum() {
		return stnum;
	}
	public void setStnum(int stnum) {
		this.stnum = stnum;
	}
	public String getStname() {
		return stname;
	}
	public void setStname(String stname) {
		this.stname = stname;
	}
	public String getStposition() {
		return stposition;
	}
	public void setStposition(String stposition) {
		this.stposition = stposition;
	}
	public String getStnatl() {
		return stnatl;
	}
	public void setStnatl(String stnatl) {
		this.stnatl = stnatl;
	}
	public String getStstory() {
		return ststory;
	}
	public void setStstory(String ststory) {
		this.ststory = ststory;
	}



	
}